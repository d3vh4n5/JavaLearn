package clase25;

public class FuncionSumar {
    // Zona de funciones

    // Cuando devuelve un dato, en lugar de void, ponemos el tipo de dato que edvuelve
    public static int suma(int num1, int num2){
        return num1 + num2;
    }
    public static void main(String[] args) {
        System.out.println(suma(5, 44));
    }
}
